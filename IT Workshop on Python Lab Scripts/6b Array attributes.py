import numpy as np
a = np.array([[1, 2], [3, 4], [5, 6], [7, 8]])

print("Array attributes")
print("Shape of the array=", a.shape)
print("Dimension of the array=", a.ndim)
print("Each element size in the array=", a.itemsize)
print("Type of the array=", a.dtype)
print("Total no of elements=", a.size)
